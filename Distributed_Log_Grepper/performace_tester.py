for word in words:
    periods = []
    ans = []

    # running the same query for 5 times.
    for ite in range(5):
        # Getting the answer for the query through client
        ans = api(None, word, cached=0)
        # taking the average of total query time taken for 10 servers
        periods.append(ans[5] / 10)
    # mean of the 5 trails
    words[word]["mean"] = mean(periods)

    # standard deviation of the 5 trails
    words[word]["sd"] = stdev(periods)

    # Cumulative Matching lines count of the query.
    words[word]["count"] = ans[3]

    # length of the query string
    words[word]["length"] = len(word)

